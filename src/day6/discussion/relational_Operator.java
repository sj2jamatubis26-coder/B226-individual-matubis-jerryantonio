
package day6.discussion;


public class relational_Operator {
    public static void main(String[] args) {
        // < > <= >= == 
        
        int num = 100;
        int num2 = 200;
        
        // boolean result = num < num2;
        
        System.out.println(num < num2);
        System.out.println(num > num2);
        System.out.println(num <= num2);
        System.out.println(num >= num2);
        System.out.println(num == num2);
        System.out.println(num != num2);
        
        
        
        
    }
    
}
