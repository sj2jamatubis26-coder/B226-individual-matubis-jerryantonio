
package day5.discussion;


public class AssignmentOperator {
    
    public static void main(String[] args) {
        int num = 10; // simple assignment
        
        num += 10;  // compound assignment
        num -= 5;
        num *= 5;
        num /= 5;
        num %= 2;
        System.out.println(num);
    }
    
}
